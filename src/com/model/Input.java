package com.model;

public class Input {
	private String from;
	private String to;
	private String mode;
	
	public void setFrom(String from) {
		this.from=from;
	}
	
	public String getFrom() {
		return this.from;
	}
	
	public void setTo(String to) {
		this.to=to;
	}
	
	public String getTo() {
		return this.to;
	}
	
	public void setMode(String mode) {
		this.mode=mode;
	}
	
	public String getMode() {
		return this.mode;
	}

}
